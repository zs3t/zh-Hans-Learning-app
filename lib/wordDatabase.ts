// 获取汉字的词语朗读内容
export function getWordForReading(char: string, pinyin: string): string {
  const charWords = wordDatabase[char];
  if (!charWords) {
    console.log(`词语数据库中未找到汉字 "${char}"`);
    return char; // 如果没有找到词语，直接返回汉字
  }

  // 尝试精确匹配拼音
  let words = charWords[pinyin];

  // 如果精确匹配失败，尝试去掉声调匹配
  if (!words || words.length === 0) {
    const pinyinWithoutTone = pinyin.replace(/[āáǎàēéěèīíǐìōóǒòūúǔùüǖǘǚǜ]/g, (match) => {
      const toneMap: Record<string, string> = {
        'ā': 'a', 'á': 'a', 'ǎ': 'a', 'à': 'a',
        'ē': 'e', 'é': 'e', 'ě': 'e', 'è': 'e',
        'ī': 'i', 'í': 'i', 'ǐ': 'i', 'ì': 'i',
        'ō': 'o', 'ó': 'o', 'ǒ': 'o', 'ò': 'o',
        'ū': 'u', 'ú': 'u', 'ǔ': 'u', 'ù': 'u',
        'ü': 'v', 'ǖ': 'v', 'ǘ': 'v', 'ǚ': 'v', 'ǜ': 'v'
      };
      return toneMap[match] || match;
    });

    // 尝试找到匹配的拼音（忽略声调）
    for (const [key, value] of Object.entries(charWords)) {
      const keyWithoutTone = key.replace(/[āáǎàēéěèīíǐìōóǒòūúǔùüǖǘǚǜ]/g, (match) => {
        const toneMap: Record<string, string> = {
          'ā': 'a', 'á': 'a', 'ǎ': 'a', 'à': 'a',
          'ē': 'e', 'é': 'e', 'ě': 'e', 'è': 'e',
          'ī': 'i', 'í': 'i', 'ǐ': 'i', 'ì': 'i',
          'ō': 'o', 'ó': 'o', 'ǒ': 'o', 'ò': 'o',
          'ū': 'u', 'ú': 'u', 'ǔ': 'u', 'ù': 'u',
          'ü': 'v', 'ǖ': 'v', 'ǘ': 'v', 'ǚ': 'v', 'ǜ': 'v'
        };
        return toneMap[match] || match;
      });

      if (keyWithoutTone === pinyinWithoutTone) {
        words = value;
        break;
      }
    }
  }

  if (!words || words.length === 0) {
    // 如果还是没有找到，使用第一个可用的词语
    const allWords = Object.values(charWords).flat();
    if (allWords.length > 0) {
      console.log(`汉字 "${char}" 使用第一个可用词语: ${allWords[0]}`);
      return allWords[0];
    }
    console.log(`汉字 "${char}" 没有找到任何词语，返回原字符`);
    return char;
  }

  // 随机选择一个词语
  const randomIndex = Math.floor(Math.random() * words.length);
  const selectedWord = words[randomIndex];
  console.log(`汉字 "${char}" (${pinyin}) -> 词语 "${selectedWord}"`);
  return selectedWord;
}
